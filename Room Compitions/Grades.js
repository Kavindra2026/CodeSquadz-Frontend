var number = prompt("Enter number");
if (number >= 90) {
  console.log("Invalid Input");
} else if (number >= 90) {
  console.log("Grade A");
} else if (number >= 80) {
  console.log("Grade B");
} else if (number >= 70) {
  console.log("Grade C");
} else if (number >= 60) {
  console.log("Grade D");
} else if (number >= 50) {
  console.log("Grade E");
} else if (number >= 40) {
  console.log("Grade F");
} else if (number >= 30) {
  console.log("Grade G");
} else if (number <= 30) {
  console.log("Grade  Fail");
}
